// ---------------------------------------------------------------------------
// ptrace memory read/write
// ---------------------------------------------------------------------------

static int ptrace_read(pid_t pid, uintptr_t addr, void *buf, size_t len) {
  size_t i = 0;
  auto *dst = (uint8_t *)buf;
  for (; i + sizeof(long) <= len; i += sizeof(long)) {
    errno = 0;
    long val = ptrace(PTRACE_PEEKDATA, pid, (void *)(addr + i), nullptr);
    if (errno)
      return -1;
    memcpy(dst + i, &val, sizeof(long));
  }
  if (i < len) {
    errno = 0;
    long val = ptrace(PTRACE_PEEKDATA, pid, (void *)(addr + i), nullptr);
    if (errno)
      return -1;
    memcpy(dst + i, &val, len - i);
  }
  return 0;
}

static int ptrace_write(pid_t pid, uintptr_t addr, const void *buf,
                        size_t len) {
  size_t i = 0;
  auto *src = (const uint8_t *)buf;
  for (; i + sizeof(long) <= len; i += sizeof(long)) {
    long val;
    memcpy(&val, src + i, sizeof(long));
    if (ptrace(PTRACE_POKEDATA, pid, (void *)(addr + i), (void *)val) < 0)
      return -1;
  }
  if (i < len) {
    errno = 0;
    long val = ptrace(PTRACE_PEEKDATA, pid, (void *)(addr + i), nullptr);
    if (errno)
      return -1;
    memcpy(&val, src + i, len - i);
    if (ptrace(PTRACE_POKEDATA, pid, (void *)(addr + i), (void *)val) < 0)
      return -1;
  }
  return 0;
}

