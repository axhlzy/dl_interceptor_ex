static int do_inject(pid_t pid, const char *so_path, const char *pkg_name,
                     bp_spec *breakpoints, int num_bp, bp_spec *dumppoints,
                     int num_dp, bp_spec *hookpoints, int num_hk,
                     bp_spec *tracepoints, int num_tr) {
  LOGI("attaching to pid %d", pid);
  if (ptrace(PTRACE_ATTACH, pid, nullptr, nullptr) < 0) {
    LOGE("PTRACE_ATTACH failed: %s", strerror(errno));
    return -1;
  }
  int status;
  if (waitpid(pid, &status, 0) < 0) {
    LOGE("waitpid after attach failed: %s", strerror(errno));
    ptrace(PTRACE_DETACH, pid, nullptr, nullptr);
    return -1;
  }
  LOGI("attached, process stopped");

  const char *libc_name = "libc.so";
  const char *libdl_name = "libdl.so";

  uintptr_t remote_mmap = get_remote_func_addr(pid, libc_name, (void *)mmap);
  uintptr_t remote_munmap =
      get_remote_func_addr(pid, libc_name, (void *)munmap);
  uintptr_t remote_dlopen =
      get_remote_func_addr(pid, libdl_name, (void *)dlopen);
  uintptr_t remote_dlsym = get_remote_func_addr(pid, libdl_name, (void *)dlsym);
  uintptr_t remote_dlerror =
      get_remote_func_addr(pid, libdl_name, (void *)dlerror);

  if (!remote_mmap || !remote_dlopen || !remote_dlsym) {
    LOGE("failed to resolve remote functions (mmap=%p, dlopen=%p, dlsym=%p)",
         (void *)remote_mmap, (void *)remote_dlopen, (void *)remote_dlsym);
    ptrace(PTRACE_DETACH, pid, nullptr, nullptr);
    return -1;
  }

  LOGI("remote mmap=%p dlopen=%p dlsym=%p", (void *)remote_mmap,
       (void *)remote_dlopen, (void *)remote_dlsym);

  // mmap a page in target
  size_t alloc_size = 0x1000;
  uint64_t mmap_args[6] = {0,
                           alloc_size,
                           PROT_READ | PROT_WRITE,
                           MAP_PRIVATE | MAP_ANONYMOUS,
                           (uint64_t)-1,
                           0};
  uint64_t remote_buf = ptrace_call(pid, remote_mmap, mmap_args, 6);
  if (remote_buf == (uint64_t)-1 || remote_buf == (uint64_t)MAP_FAILED) {
    LOGE("remote mmap failed");
    ptrace(PTRACE_DETACH, pid, nullptr, nullptr);
    return -1;
  }

  // dlopen
  size_t path_len = strlen(so_path) + 1;
  ptrace_write(pid, (uintptr_t)remote_buf, so_path, path_len);
  uint64_t dlopen_args[2] = {remote_buf, RTLD_NOW};
  LOGI("calling dlopen(\"%s\") in target", so_path);
  uint64_t handle = ptrace_call(pid, remote_dlopen, dlopen_args, 2);

  if (handle == 0) {
    LOGE("dlopen returned NULL");
    if (remote_dlerror) {
      uint64_t no_args[1] = {0};
      uint64_t err_ptr = ptrace_call(pid, remote_dlerror, no_args, 0);
      if (err_ptr) {
        char msg[256] = {};
        ptrace_read(pid, (uintptr_t)err_ptr, msg, 255);
        LOGE("dlerror: %s", msg);
      }
    }
    uint64_t munmap_args[2] = {remote_buf, alloc_size};
    ptrace_call(pid, remote_munmap, munmap_args, 2);
    ptrace(PTRACE_DETACH, pid, nullptr, nullptr);
    return -1;
  }
  LOGI("dlopen succeeded, handle=%p", (void *)handle);

  // Helper lambda: dlsym a symbol from the injected .so
  auto remote_resolve = [&](const char *sym_name) -> uint64_t {
    ptrace_write(pid, (uintptr_t)remote_buf, sym_name, strlen(sym_name) + 1);
    uint64_t dlsym_args[2] = {handle, remote_buf};
    return ptrace_call(pid, remote_dlsym, dlsym_args, 2);
  };

  // Call dl_interceptor_init()
  uint64_t init_addr = remote_resolve("dl_interceptor_init");
  if (init_addr == 0) {
    LOGE("dlsym(\"dl_interceptor_init\") returned NULL");
  } else {
    LOGI("calling dl_interceptor_init() at %p", (void *)init_addr);
    uint64_t no_args[1] = {0};
    uint64_t ret = ptrace_call(pid, (uintptr_t)init_addr, no_args, 0);
    if (ret == 0)
      LOGI("dl_interceptor_init() succeeded");
    else
      LOGE("dl_interceptor_init() returned %d", (int)(int64_t)ret);
  }

  // Set package name in lib
  if (pkg_name) {
    uint64_t set_pkg_addr = remote_resolve("dl_interceptor_set_package_name");
    if (set_pkg_addr != 0) {
      ptrace_write(pid, (uintptr_t)remote_buf, pkg_name, strlen(pkg_name) + 1);
      uint64_t pkg_args[1] = {remote_buf};
      ptrace_call(pid, (uintptr_t)set_pkg_addr, pkg_args, 1);
    }
  }

  // Set breakpoints if any
  if (num_bp > 0) {
    uint64_t bp_func = remote_resolve("dl_interceptor_set_breakpoint");
    if (bp_func == 0) {
      LOGE("dlsym(\"dl_interceptor_set_breakpoint\") returned NULL");
    } else {
      for (int i = 0; i < num_bp; i++) {
        ptrace_write(pid, (uintptr_t)remote_buf, breakpoints[i].lib_name,
                     strlen(breakpoints[i].lib_name) + 1);
        uint64_t bp_args[2] = {remote_buf,
                               (uint64_t)(int64_t)breakpoints[i].index};
        LOGI("setting breakpoint: %s:%d", breakpoints[i].lib_name,
             breakpoints[i].index);
        uint64_t ret = ptrace_call(pid, (uintptr_t)bp_func, bp_args, 2);
        if (ret == 0)
          LOGI("breakpoint set OK");
        else
          LOGE("breakpoint set failed (ret=%d)", (int)(int64_t)ret);
      }
    }
  }

  // Set dumppoints if any
  if (num_dp > 0) {
    uint64_t dp_func = remote_resolve("dl_interceptor_set_dumppoint");
    if (dp_func == 0) {
      LOGE("dlsym(\"dl_interceptor_set_dumppoint\") returned NULL");
    } else {
      for (int i = 0; i < num_dp; i++) {
        ptrace_write(pid, (uintptr_t)remote_buf, dumppoints[i].lib_name,
                     strlen(dumppoints[i].lib_name) + 1);
        uint64_t dp_args[2] = {remote_buf,
                               (uint64_t)(int64_t)dumppoints[i].index};
        LOGI("setting dumppoint: %s:%d", dumppoints[i].lib_name,
             dumppoints[i].index);
        uint64_t ret = ptrace_call(pid, (uintptr_t)dp_func, dp_args, 2);
        if (ret == 0)
          LOGI("dumppoint set OK");
        else
          LOGE("dumppoint set failed (ret=%d)", (int)(int64_t)ret);
      }
    }
  }

  // Set hookpoints if any
  if (num_hk > 0) {
    uint64_t hk_func = remote_resolve("dl_interceptor_set_hookpoint");
    if (hk_func == 0) {
      LOGE("dlsym(\"dl_interceptor_set_hookpoint\") returned NULL");
    } else {
      for (int i = 0; i < num_hk; i++) {
        ptrace_write(pid, (uintptr_t)remote_buf, hookpoints[i].lib_name,
                     strlen(hookpoints[i].lib_name) + 1);
        uint64_t hk_args[1] = {remote_buf};
        LOGI("setting hookpoint: %s", hookpoints[i].lib_name);
        uint64_t ret = ptrace_call(pid, (uintptr_t)hk_func, hk_args, 1);
        if (ret == 0)
          LOGI("hookpoint set OK");
        else
          LOGE("hookpoint set failed (ret=%d)", (int)(int64_t)ret);
      }
    }
  }

  // Set tracepoints if any
  if (num_tr > 0) {
    uint64_t tr_func = remote_resolve("dl_interceptor_set_tracepoint");
    if (tr_func == 0) {
      LOGE("dlsym(\"dl_interceptor_set_tracepoint\") returned NULL");
    } else {
      for (int i = 0; i < num_tr; i++) {
        ptrace_write(pid, (uintptr_t)remote_buf, tracepoints[i].lib_name,
                     strlen(tracepoints[i].lib_name) + 1);
        uint64_t tr_args[2] = {remote_buf,
                               (uint64_t)(int64_t)tracepoints[i].index};
        LOGI("setting tracepoint: %s:%d", tracepoints[i].lib_name,
             tracepoints[i].index);
        uint64_t ret = ptrace_call(pid, (uintptr_t)tr_func, tr_args, 2);
        if (ret == 0)
          LOGI("tracepoint set OK");
        else
          LOGE("tracepoint set failed (ret=%d)", (int)(int64_t)ret);
      }
    }
  }

  // Cleanup
  uint64_t munmap_args[2] = {remote_buf, alloc_size};
  ptrace_call(pid, remote_munmap, munmap_args, 2);
  ptrace(PTRACE_DETACH, pid, nullptr, nullptr);
  LOGI("detached from pid %d", pid);
  return handle ? 0 : -1;
}

