static pid_t find_pid_by_name(const char *pkg_name) {
  DIR *dir = opendir("/proc");
  if (!dir)
    return -1;
  struct dirent *entry;
  while ((entry = readdir(dir)) != nullptr) {
    if (entry->d_type != DT_DIR)
      continue;
    char *end;
    long pid = strtol(entry->d_name, &end, 10);
    if (*end != '\0' || pid <= 0)
      continue;
    char path[256];
    snprintf(path, sizeof(path), "/proc/%ld/cmdline", pid);
    FILE *fp = fopen(path, "r");
    if (!fp)
      continue;
    char cmdline[256] = {};
    fread(cmdline, 1, sizeof(cmdline) - 1, fp);
    fclose(fp);
    if (strcmp(cmdline, pkg_name) == 0) {
      closedir(dir);
      return (pid_t)pid;
    }
  }
  closedir(dir);
  return -1;
}

static pid_t wait_for_process(const char *pkg_name, int timeout_ms) {
  int elapsed = 0;
  const int interval_ms = 5;
  while (elapsed < timeout_ms) {
    pid_t pid = find_pid_by_name(pkg_name);
    if (pid > 0)
      return pid;
    usleep(interval_ms * 1000);
    elapsed += interval_ms;
  }
  return -1;
}

static int start_app(const char *pkg_name, const char *activity) {
  char cmd[512];
  if (activity) {
    snprintf(cmd, sizeof(cmd), "am start -n %s/%s", pkg_name, activity);
  } else {
    char resolve_cmd[256];
    snprintf(resolve_cmd, sizeof(resolve_cmd),
             "cmd package resolve-activity --brief -c "
             "android.intent.category.LAUNCHER %s 2>/dev/null | tail -1",
             pkg_name);
    FILE *fp = popen(resolve_cmd, "r");
    if (fp) {
      char component[256] = {};
      if (fgets(component, sizeof(component), fp)) {
        size_t len = strlen(component);
        while (len > 0 &&
               (component[len - 1] == '\n' || component[len - 1] == '\r'))
          component[--len] = '\0';
        pclose(fp);
        if (len > 0 && strchr(component, '/')) {
          snprintf(cmd, sizeof(cmd), "am start -n %s", component);
          LOGI("starting app: %s", cmd);
          return system(cmd);
        }
      } else {
        pclose(fp);
      }
    }
    snprintf(
        cmd, sizeof(cmd),
        "monkey -p %s -c android.intent.category.LAUNCHER 1 >/dev/null 2>&1",
        pkg_name);
  }
  LOGI("starting app: %s", cmd);
  return system(cmd);
}

static void force_stop_app(const char *pkg_name) {
  char cmd[256];
  snprintf(cmd, sizeof(cmd), "am force-stop %s", pkg_name);
  system(cmd);
  usleep(500 * 1000);
}

