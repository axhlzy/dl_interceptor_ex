#define MAX_BREAKPOINTS 32

struct bp_spec {
  char lib_name[128];
  int index; // -1 = all
};

static int parse_bp_spec(const char *str, bp_spec *out) {
  // Format: "libfoo.so:N" or "libfoo.so" (all entries) or "libfoo.so:*"
  const char *colon = strchr(str, ':');
  if (!colon) {
    // No colon: break on all entries
    strncpy(out->lib_name, str, sizeof(out->lib_name) - 1);
    out->lib_name[sizeof(out->lib_name) - 1] = '\0';
    out->index = -1;
    return 0;
  }
  size_t name_len = (size_t)(colon - str);
  if (name_len >= sizeof(out->lib_name))
    name_len = sizeof(out->lib_name) - 1;
  memcpy(out->lib_name, str, name_len);
  out->lib_name[name_len] = '\0';

  const char *idx_str = colon + 1;
  if (strcmp(idx_str, "*") == 0) {
    out->index = -1;
  } else {
    out->index = atoi(idx_str);
  }
  return 0;
}

